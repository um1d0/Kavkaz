let currentLang = "en";
let currentCategory = "appetizers";

const langButtons = document.querySelectorAll(".lang-switch button");
const navLinks = document.querySelectorAll(".nav-links a");
const categoryCards = document.querySelectorAll(".cat-card");
const pageSections = document.querySelectorAll(".page-section");
const menuGrid = document.getElementById("menu-grid");
const menuToggle = document.querySelector(".menu-toggle");
const topNav = document.querySelector(".top-nav");

function changeLanguage(lang) {
  currentLang = lang;
  const elements = document.querySelectorAll("[data-key]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-key");
    if (translations[lang] && translations[lang][key]) {
      if (typeof translations[lang][key] === "object") {
        el.textContent = translations[lang][key].name;
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });

  langButtons.forEach((button) => {
    button.classList.toggle("active-lang", button.dataset.lang === lang);
  });

  localStorage.setItem("preferredLang", lang);
  renderMenu();
}

function showPage(pageId) {
  pageSections.forEach((section) => {
    section.classList.toggle("active-page", section.id === `${pageId}-page`);
  });

  navLinks.forEach((link) => {
    link.classList.toggle("active-link", link.dataset.page === pageId);
  });

  closeMobileMenu();
}

function toggleMobileMenu() {
  topNav.classList.toggle("open");
  const isOpen = topNav.classList.contains("open");
  menuToggle.setAttribute("aria-expanded", isOpen.toString());

  const icon = menuToggle.querySelector("i");
  if (icon) {
    icon.className = isOpen ? "fas fa-times" : "fas fa-bars";
  }
}

function closeMobileMenu() {
  if (topNav.classList.contains("open")) {
    topNav.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");

    const icon = menuToggle.querySelector("i");
    if (icon) {
      icon.className = "fas fa-bars";
    }
  }
}

function setActiveCategory(category, element) {
  currentCategory = category;
  categoryCards.forEach((card) => card.classList.remove("active-cat"));
  if (element) {
    element.classList.add("active-cat");
  }
  renderMenu();
}

function renderMenu() {
  const items = menuItems[currentCategory] || [];
  menuGrid.innerHTML = items
    .map((key) => {
      const translatedItem =
        translations[currentLang][key] || translations.en[key] || {};
      const imageSrc =
        translatedItem.img ||
        (translations.en[key] && translations.en[key].img) ||
        "";
      return `
            <div class="product">
                ${imageSrc ? `<img src='${imageSrc}' alt="${translatedItem.name || ""}">` : ""}
                <h3>${translatedItem.name || ""}</h3>
                <span>${translations.en[key].price || ""}</span>
            </div>
        `;
    })
    .join("");
}

function initializePage() {
  const savedLang = localStorage.getItem("preferredLang") || "en";
  changeLanguage(savedLang);
  showPage("home");
  setActiveCategory("appetizers", document.querySelector(".cat-card"));

  if (menuToggle) {
    menuToggle.addEventListener("click", toggleMobileMenu);
  }
}

window.addEventListener("DOMContentLoaded", initializePage);
